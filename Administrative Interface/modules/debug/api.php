/**
 * API call to fetch blocks listing
 */
framework_add_hook ( "debug_explain", "debug_explain");
framework_add_permission ( "debug_explain", __ ( "Debug E.164 number"));
framework_add_api_call ( "/debug/explain/:number", "Read", "debug_explain", array ( "permissions" => array ( "user", "debug_explain")));

/**
 * Function to explain an E.164 number.
 *
 * @global array $_in Framework global configuration variable
 * @param string $buffer Buffer from plugin system if processed by other function
 *                       before
 * @param array $parameters Optional parameters to the function
 * @return string Output of the generated page
 */
function debug_explain ( $buffer, $parameters)
{
  global $_in;

  /**
   * Return structured data
   */
  return array_merge_recursive ( ( is_array ( $buffer) ? $buffer : array ()), filters_call ( "e164_identify", array ( "number" => $parameters["number"])));
}
