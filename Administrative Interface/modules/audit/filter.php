/**
 * Add block's filters
 */
framework_add_filter ( "page_menu_reports", "audit_menu");

/**
 * Function to add entry to registers menu.
 *
 * @param string $buffer Buffer from plugin system if processed by other function
 *                       before
 * @param array $parameters Optional parameters to the function
 * @return array Menus structure
 */
function audit_menu ( $buffer, $parameters)
{
  return array_merge ( (array) $buffer, array ( array ( "type" => "entry", "group" => "audit", "icon" => "eye", "href" => "/reports/auditory", "text" => __ ( "Auditory"), "permission" => "audit")));
}
